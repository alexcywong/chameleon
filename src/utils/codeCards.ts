/**
 * Converts a grid coordinate like "A3" to a word index (0-15).
 * A1=0, A2=1, A3=2, A4=3, B1=4, ... D4=15
 */
export function coordinateToIndex(coord: string): number {
  const row = coord.charCodeAt(0) - 65; // A=0, B=1, C=2, D=3
  const col = parseInt(coord[1]) - 1;   // 1=0, 2=1, 3=2, 4=3
  return row * 4 + col;
}

/**
 * Given a code card index and dice values, returns the secret word index (0-15).
 */
export function getSecretWordIndex(
  codeCardIndex: number,
  diceYellow: number,
  diceBlue: number
): number {
  const card = codeCards[codeCardIndex % codeCards.length];
  const key = `${diceYellow},${diceBlue}`;
  const coord = card[key];
  if (!coord) return 0;
  return coordinateToIndex(coord);
}

/**
 * Given a code card index and dice values, returns the coordinate string (e.g. "A3").
 */
export function getCoordinate(
  codeCardIndex: number,
  diceYellow: number,
  diceBlue: number
): string {
  const card = codeCards[codeCardIndex % codeCards.length];
  const key = `${diceYellow},${diceBlue}`;
  return card[key] || 'A1';
}

export function getCodeCardCount(): number {
  return codeCards.length;
}
